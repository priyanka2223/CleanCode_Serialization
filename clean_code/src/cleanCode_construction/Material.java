package cleanCode_construction;

public class Material {

	public double standard(double feet) {
		return feet*1200;
	}

	public double aboveStandard(double feet) {
		return feet*1500;
	}

	public double highStandard(double feet) {
		return feet*1800;
	}

	public double highStandardFullyAutomated(double feet) {
		return feet*2500;
	}

}
